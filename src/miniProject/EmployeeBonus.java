    private void btncalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalculateActionPerformed
        // TODO add your handling code here:
        
        String id = txtId.getText();
        String name = txtName.getText();
        String surname = txtSurname.getText();
        String position = (String)cbPosition.getSelectedItem();
        float salary,bonus;
        salary = Float.parseFloat(txtSalary.getText());
        switch (position) {
            case "Manager":
                if(salary < 10000){
                    bonus = (float) (salary*0.05);
                }else{
                    bonus = (float) (salary*0.10);
                }   break;
            case "Assistant Manager":
                if(salary < 10000){
                    bonus = (float) (salary*0.15);
                }else{
                    bonus = (float) (salary*0.20);
                }   break;
            default:
                if(salary < 10000){
                    bonus = (float) (salary*0.25);
                }else{
                    bonus = (float) (salary*0.30);
                }   break;
        }
        txtBonus.setText(String.valueOf(bonus));
        
    }//GEN-LAST:event_btncalculateActionPerformed

